/**
* IE浏览器检测
* 
* 返回值     值类型    值说明
*    -1      Number    不是ie浏览器
*    6       Number    ie版本<=6
*    7       Number    ie7
*    8       Number    ie8
*    9       Number    ie9
*   10       Number    ie10
*   11       Number    ie11
*  'edge'    String    ie的edge浏览器
*/
function IEVersion() {
  var userAgent = navigator.userAgent; //取得浏览器的userAgent字符串  
  var isIE = userAgent.indexOf("compatible") > -1 && userAgent.indexOf("MSIE") > -1; //判断是否IE<11浏览器  
  var isEdge = userAgent.indexOf("Edge") > -1 && !isIE; //判断是否IE的Edge浏览器  
  var isIE11 = userAgent.indexOf('Trident') > -1 && userAgent.indexOf("rv:11.0") > -1;
  if (isIE) {
    var fIEVersion = userAgent.match(/MSIE (\d+)/)[1];
    if (fIEVersion == 7) {
      return 7;
    } else if (fIEVersion == 8) {
      return 8;
    } else if (fIEVersion == 9) {
      return 9;
    } else if (fIEVersion == 10) {
      return 10;
    } else {
      return 6; //IE版本<=7
    }
  } else if (isEdge) {
    return 'edge'; //edge
  } else if (isIE11) {
    return 11; //IE11  
  } else {
    return -1; //不是ie浏览器
  }
}

// Fix input placeholder issue for IE8 and IE9
export function FixInputPlaceholderForIE() {
  let isIE8 = IEVersion() == 8 ? true : false;
  let isIE9 = IEVersion() == 9 ? true : false;
  //fix html5 placeholder attribute for ie7 & ie8
  if (isIE8 || isIE9) {
    // ie8 & ie9
    // this is html5 placeholder fix for inputs, inputs with placeholder-no-fix class will be skipped(e.g: we need this for password fields)
    $(
      'input[placeholder]:not(.placeholder-no-fix), textarea[placeholder]:not(.placeholder-no-fix)'
    ).each(function() {
      var input = $(this);

      if (input.val() === '' && input.attr('placeholder') !== '') {
        input.addClass('placeholder').val(input.attr('placeholder'));
      }

      input.focus(function() {
        if (input.val() == input.attr('placeholder')) {
          input.val('');
        }
      });

      input.blur(function() {
        if (input.val() === '' || input.val() == input.attr('placeholder')) {
          input.val(input.attr('placeholder'));
        }
      });
    });
  }
}
